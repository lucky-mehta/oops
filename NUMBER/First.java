//WAP TO PRINT 1 TO 100 NATURAL NUMBER.

public class First {

    public static void main(String[] args) {
        int i = 1;
        System.out.println(" 1 to 100 natural number is:");
        while (i <= 100) {
            System.out.println("Number is:" + i);
            i = i + 1;
        }
    }
}
